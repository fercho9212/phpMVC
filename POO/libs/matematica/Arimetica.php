<?php
namespace Libs\Matematica;

class Arimetica{
    public function suma($a,$b){
        return $a+$b;
    }
    public function resta($a,$b){
        return $a-$b;
    }
    public function multiplicación($a,$b){
        return $a*$b;
    }
    public function division($a,$b){
        return $a/$b;
    }

}
?>